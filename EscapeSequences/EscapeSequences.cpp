#include "pch.h"
#include <iostream>

int main()
{
    std::cout << "How following escape sequences works ??\n\n";
   
    std::cout << "Bell or Alert   :     "           << "Here we go !!\a Have you heard that alert? " << std::endl;
    std::cout << "Backspace :           "           << "Hi my name is Sarvagya and\b I used Backspace here. " << std::endl;
    std::cout << "Form feed :           "           << "Hi my name is Sarvagya and\f i am programming here.. " << std::endl;
    std::cout << "New Line :            "           << "Hi my name is Sarvagya and\n i am programming here.. " << std::endl;
    std::cout << "Carriage Return\n\n :     "           << "Hi my name is Sarvagya and\r i am programming here.. " << std::endl;
    std::cout << "Horizontal tab :      "           << "Hi my name is Sarvagya and\t i am programming here.. " << std::endl;
    std::cout << "Vertical tab :        "           << "Hi my name is Sarvagya and\v i am programming here.. " << std::endl;
    std::cout << "Single Quotation :    "           << "Hi my name is Sarvagya and\' i am programming here.. " << std::endl;
    std::cout << "Double Quotation :    "           << "Hi my name is Sarvagya and\" i am programming here.. " << std::endl;
    std::cout << "Backslash :           "           << "Hi my name is Sarvagya and\\ i am programming here.. " << std::endl;
    std::cout << "Literal Question mark:"           << "Hi my name is Sarvagya and\? i am programming here.. " << std::endl;

}

